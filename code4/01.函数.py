# 函数
# 函数简介
#   函数也是一个对象
#   对象是内存中专门存储数据的一块区域
#   函数可以用来保存一些可执行的代码
#   创建函数:
#       def 函数名([形参1,形参2,...]):
#           代码块
#   函数名必须要符合标识符的规范
#       (可以包含字母、数字、下划线、但是不能以数字开头)

# 定义一个函数
def fn():
    print('函数1111!')
    print('hello')
    print('end!')

# 打印fn
# print(type(fn))

# 调用函数
