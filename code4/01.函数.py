# fn()
# 调用函数时，填入实参

# 定义一个函数，可以用来求任意两个数的和
# 函数的参数:
#   函数名后的()中定义数量不等的形参,
#       多个形参之间用,隔开
def fn2(a , b) :
    print('a =',a)
    print('b =',b)
    print(a,'+',b,'=',a+b)

# fn2(10,20)

